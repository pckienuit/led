void WS2812_Send (void) {
	uint32_t indx = 0;
	uint32_t color;

	for (int i = 0; i < MAX_LED; ++i) {
		color = ((LED_Mod[i][1] << 16) | (LED_Mod[i][2] << 8) | (LED_Mod[i][3]));

		for (int j = 23; j >= 0; j--) {
			if (color&(1<<j)) {
				pwmData[indx] = 60;
			} else {
				pwmData[indx] = 30;
			}

			indx++;
		}
	}

	for (int i = 0; i < 50; ++i) {
		pwmData[indx] = 0;
		indx++;
	}

	// Reset flag before starting DMA
	datasentflag = 0;
	
	HAL_TIM_PWM_Start_DMA(&htim1, TIM_CHANNEL_1, (uint32_t *)pwmData, indx);
	
	// Add timeout to prevent infinite loop
	uint32_t timeout = 1000000;  // Adjust timeout value
	while(!datasentflag && timeout > 0) {
		timeout--;
	}
	
	// Force stop if timeout occurred
	if (timeout == 0) {
		HAL_TIM_PWM_Stop_DMA(&htim1, TIM_CHANNEL_1);
		datasentflag = 1;
	}
	
	datasentflag = 0;
}

#define LED_DELAY 10

void Fade_Effect (int Red, int Green, int Blue, int speed) { //speed 1 -> 10
	if (speed > 10) speed = 10;
	if (speed < 1)	speed = 1;

	Set_All_LEDs_Same_Color(Red, Green, Blue);

	for (int i = 0; i <= 100; ++i) {
		Set_Brightness(i);
		WS2812_Send();
		HAL_Delay(LED_DELAY/speed);
	}

	for (int i = 100; i >= 0; --i) {
		Set_Brightness(i);
		WS2812_Send();
		HAL_Delay(LED_DELAY/speed);
	}
}

void Rainbow_Effect (int speed) {
	for (int r = 0; r <= 255; r+=10) {
		for (int g = 0; g <= 255; g+=10){
			for (int b = 0; b <= 255; b+=10) {
				Set_LED(1, r, g, b);
				Set_Brightness(100);
				WS2812_Send();
				HAL_Delay(10);
			}
		}
	}
}

